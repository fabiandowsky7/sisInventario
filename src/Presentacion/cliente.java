    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtrut.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Rut para el cliente");
            txtrut.requestFocus();
            return;
        }
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un Nombre para el cliente");
            txtnombre.requestFocus();
            return;
        }
        if (txtapaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un apellido para el cliente");
            txtapaterno.requestFocus();
            return;
        }
        if (txtamaterno.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un apellido para el cliente");
            txtamaterno.requestFocus();
            return;
        }
        if (txtdireccion.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una direccion");
            txtdireccion.requestFocus();
            return;
        }
        if (txtemail.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un email");
            txtemail.requestFocus();
            return;
        }

        if (txttelefono.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un telefono");
            txttelefono.requestFocus();
            return;
        }

        if (txtcodigo.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar un codigo para el cliente");
            txtcodigo.requestFocus();
            return;
        }
        if (txtobservacion.getText().length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar una observacion para el cliente");
            txtobservacion.requestFocus();
            return;
        }
        vcliente dts = new vcliente();
        fcliente func = new fcliente();

        dts.setRut(txtrut.getText());
        dts.setNombre(txtnombre.getText());
        dts.setApaterno(txtapaterno.getText());
        dts.setAmaterno(txtamaterno.getText());
        dts.setDireccion(txtdireccion.getText());
        dts.setEmail(txtemail.getText());
        dts.setTelefono(Integer.parseInt(txttelefono.getText()));
        dts.setCodigo(txtcodigo.getText());
        dts.setDeuda(Integer.parseInt(txtdeuda.getText()));
        dts.setObservaciones(txtobservacion.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "el cliente fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        }

    }//GEN-LAST:event_btnguardarActionPerformed

